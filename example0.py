# An example of nested if statements

x = 10
y = 20

if x > 0:
  if y > 0:
    print("Both x and y are positive numbers!")
  else:
    print("Only x is positive")
else:
  if y > 0:
    print("only y is positive")
  else:
    print("neither of them are positive")

# nested if statements allow you to run an if/conditional statement within another conditional statement.
# but there are better ways to check and see if both x and y are positive.  Look at the logical operators in the next few example files!
    
